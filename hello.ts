let message: string = "Hello World"
console.log(message)

let num: number = 10
console.log(num)

let isTrue: boolean = true
console.log(isTrue)

let tuple: [string, string] = [message, message]
console.log(tuple)

//数组
let arr: number[] = [1, 2, 3]
console.log(arr)

//字符串数组
let names: string[] = ["张三", "李四", "王五"]
console.log(names)

//任意类型数组
let anythings: any[] = [1, "2", true]
console.log(anythings)

//任意类型
let temp: any = "123"
console.log(temp)

temp = 123
console.log(temp)

temp = true
console.log(temp)

//数组
let arr2: Array<number> = [1, 2, 3]
console.log(arr2)

//空类型
let empty: void = undefined
console.log(empty)

//null和undefined
let n: null = null
let u: undefined = undefined
console.log(n)
console.log(u)

